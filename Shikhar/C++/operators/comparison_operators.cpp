#include <iostream>
using namespace std;

int main()
{
    // comparison operators
    int a = 4, b = 5;
    cout << "operators in c++" << endl;
    cout << "Following are the types of the comparison operators in c++" << endl
         << endl;

    cout << "The value of a==b is = " << (a == b) << endl;
    cout << "The value of a!=b is = " << (a != b) << endl;
    cout << "The value of a>=b is = " << (a >= b) << endl;
    cout << "The value of a<=b is = " << (a <= b) << endl;
    cout << "The value of a>b is = " << (a > b) << endl;
    cout << "The value of a<b is = " << (a < b) << endl;
    cout << endl
         << endl;

    return 0;
}