package Iteratives;

public class do_while_loop {

	public static void main(String[] args) {
		//program to print first 10 natural numbers
		//do-while loop will run the condition once even if the condition is false
		int num = 1;
		do
		{
			System.out.println(num);
			num++;
		}
		while(num <=10);

	}

}
