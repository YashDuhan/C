#include <iostream>
using namespace std;
int main()
{
								cout << "Program to check even or odd" << endl;
								cout << "Enter a number" << endl;
								int num;
								cin >> num;
								if (num%2 == 0)
								{
																cout << "The number " << num << " is an EVEN number" << endl;
								}
								else
								{
																cout << "The number " << num << " is an ODD number" << endl;
								}
								return 0;
}
